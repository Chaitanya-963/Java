public class ShapeTransfromationExe_02 {

    public static void main(String[] args) {
        double shapeValue = 42.75;

        System.out.println("Current shape [double]: " + shapeValue);

        System.out.println("Shape Shiffting Values");

        int intValue = (int) shapeValue;
        System.out.println("Int value : " + intValue);
        
        long longValue = (long) shapeValue;
        System.out.println("Long value : " + longValue);

        float floatValue = (float) shapeValue;
        System.out.println("Float value : " + floatValue);

        short shortValue = (short) shapeValue;
        System.out.println("Short value : " + shortValue);

        byte byteValue = (byte) shapeValue;
        System.out.println("Byte value : " + byteValue);


    }
    
}
